let lang = "en";

export function t(key: string, ...args: string[]): string {
  let text = dict[lang]?.[key];
  if (!text) {
    // fallback to English
    text = dict["en"]?.[key];
  }
  if (!text) return key;
  for (let i = 0; i < args.length; i++) {
    text = text.replace(`$${i + 1}`, args[i]);
  }
  return text;
}

export function setLang(newLang: string) {
  lang = newLang;
}

export function getLang(): string {
  return lang;
}
